    private void jButtonBuscarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarTodosActionPerformed
        DefaultTableModel modelo;
        String[] titulosColumnas = {"CÃ“DIGO", "NOMBRE", "APELLIDOS", "CEDULA"};
        String[][] info = {};
        ConexionConBaseDatos conectar = new ConexionConBaseDatos();
        List<Cliente> lista = controller.listarclientes();
        modelo = new DefaultTableModel(info, titulosColumnas) {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        for (Cliente cliente : lista) {
            System.out.println("View->"+cliente.getIdCliente());
            Object[] obj = {cliente.getIdCliente(),
                cliente.getNombre_Cliente(),
                cliente.getApellido_Cliente(),
                cliente.getCedula_Cliente()};
            modelo.addRow(obj);
        }
        ListarCliente.jTableListarCliente.setModel(modelo);
    }//GEN-LAST:event_jButtonBuscarTodosActionPerformed

